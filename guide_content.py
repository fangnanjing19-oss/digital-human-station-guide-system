def find_station_index(query: str) -> int:
    text = query or ""
    for i, station in enumerate(STATION_GUIDES):
        if station.title in text or any(alias in text for alias in station.aliases):
            return i
    return 0


def build_station_response(query: str) -> tuple[StationGuide, int]:
    idx = find_station_index(query)
    return STATION_GUIDES[idx], idx


def next_station_prompt(idx: int) -> str:
    next_idx = min(idx + 1, len(STATION_GUIDES) - 1)
    next_title = STATION_GUIDES[next_idx].title
    if next_idx == idx:
        return "讲解员模式：请总结整条长征展线的历史意义"
    return f"讲解员模式：站点讲解：{next_title}"
